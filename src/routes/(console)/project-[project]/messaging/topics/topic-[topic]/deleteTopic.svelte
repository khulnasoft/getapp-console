<script lang="ts">
    import { goto } from '$app/navigation';
    import { base } from '$app/paths';
    import { page } from '$app/stores';
    import { Modal } from '$lib/components';
    import { Button } from '$lib/elements/forms';
    import { addNotification } from '$lib/stores/notifications';
    import { sdk } from '$lib/stores/sdk';
    import { topic } from './store';
    import { project } from '../../../store';
    import { Submit, trackEvent, trackError } from '$lib/actions/analytics';

    export let showDelete = false;
    const deleteTopic = async () => {
        try {
            await sdk.forProject.messaging.deleteTopic($topic.$id);

            showDelete = false;
            addNotification({
                type: 'success',
                message: `${$topic.name} has been deleted`
            });
            trackEvent(Submit.MessagingTopicDelete);
            await goto(`${base}/project-${$page.params.project}/messaging/topics`);
        } catch (error) {
            addNotification({
                type: 'error',
                message: error.message
            });
            trackError(error, Submit.MessagingTopicDelete);
        }
    };
</script>

<Modal
    title="Delete topic"
    bind:show={showDelete}
    onSubmit={deleteTopic}
    icon="exclamation"
    state="warning"
    headerDivider={false}>
    <p data-private>
        Are you sure you want to delete <b>{$topic.name}</b> from '{$project.name}'?
    </p>
    <svelte:fragment slot="footer">
        <Button text on:click={() => (showDelete = false)}>Cancel</Button>
        <Button secondary submit>Delete</Button>
    </svelte:fragment>
</Modal>
